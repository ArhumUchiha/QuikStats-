import math

def sqrt(x):
    x = float(x)
    print(math.sqrt(x))

def add(x, y):
    x = float(x)
    y = float(y)
    print( x + y )

def sub(x, y):
    x = float(x)
    y = float(y)
    print ( x - y )

def multi(x, y):
    x,y = float(x), float(y)
    print( x * y )

def div(x, y):
    x,y = float(x), float(y)
    print( x / y )